-----------------------------------Inserts-----------------------------
drop table test_auto;

create table test_auto(
    test_id number generated by default on null as identity,
    constraint test primary key(test_id)
);

insert into test_auto values (20);

select * from test_auto;


--Recommended way to do an insert 
--which is list the columns in the same order that they appear in in the table
insert into departments(DEPARTMENT_ID, DEPARTMENT_NAME, MANAGER_ID, LOCATION_ID)
values                 (71, 'Development 1', 100, 1700);


--Je kan dit ook doen, yk, zonder actually te specifyen in welke rows je insert, maar als je dit doet, dan zeg je basically
--dat je in elke column van die table gaat inserten
insert into DEPARTMENTS values(72, 'Development 2', 100, 1700);


--You can also change the order in which the columns appear, maar dan moeten die order van die values ook matchen met wat je hebt gezet
insert into DEPARTMENTS(DEPARTMENT_NAME, MANAGER_ID, LOCATION_ID, DEPARTMENT_ID)
            values      ('Development 3', 100, 1700, 73);




--Inserting rows with null values
--implicit:plaats het niet in je insert lijst en dan gaat het automatisch null voor je zetten. Also make sure dat ze nullable zijn ofc
insert into DEPARTMENTS(DEPARTMENT_ID, DEPARTMENT_NAME)
                values (74, 'Development 4');


--explicit waarbij je zelf null zet
insert into DEPARTMENTS(DEPARTMENT_ID, DEPARTMENT_NAME,MANAGER_ID, LOCATION_ID)
            VALUES      (75, 'Development 5', null, null);



--Je kan substitution variables gebruiken
insert into departments(DEPARTMENT_ID, DEPARTMENT_NAME)
            VALUES      (&dept_id, '&dept_name');


--You can make inserts using a subquery
