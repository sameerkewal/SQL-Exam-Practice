-------------------------Common Errors-------------------------------


--Primary keys mogen geen duplicates zijn en moeten ook geen null zijn
--Therefore zou dit logisch niet kunnen:
--Unique Constraint PK violated
insert into departments(DEPARTMENT_ID, DEPARTMENT_NAME, MANAGER_ID, LOCATION_ID)
VALUES                  (10, 'Development 1', 100, 1700);


--Inserting FK value that doesnt exist in the parent table
--Integrity constraint violated
insert into DEPARTMENTS(DEPARTMENT_ID, DEPARTMENT_NAME, MANAGER_ID, LOCATION_ID)
VALUES                  (5, 'Development 1', 100, 1);


